"use strict";
// Intersection serve para extender um tipo
function handleProdutoCarro(dados) {
    dados.rodas;
    dados.portas;
    dados.preco;
}
handleProdutoCarro({
    preco: 20000,
    rodas: 4,
    portas: 5,
});
const dado1 = {
    preco: 20000,
    rodas: 4,
    portas: 5,
};
